    private static PieDataset createDataset() {

        DefaultPieDataset dataset = new DefaultPieDataset();
        ReporteGerencia reporte2 = new ReporteGerencia();
        ArrayList<GerenciaQuery2> resultados = reporte2.depositosAgencia();

        for (int i = 0; i < resultados.size(); i++) {
            System.out.println(resultados.get(i).getNombre_agencia() + " " + resultados.get(i).getNombre_cliente() + resultados.get(i).getCantidad_depositos());
            dataset.setValue(resultados.get(i).getNombre_agencia() + " " + resultados.get(i).getNombre_cliente(), resultados.get(i).getCantidad_depositos());

        }

        return dataset;
    }

