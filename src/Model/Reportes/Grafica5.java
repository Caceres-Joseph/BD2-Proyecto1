    private static DefaultCategoryDataset createDataset() {

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        ReporteGerencia reporte5 = new ReporteGerencia();
        ArrayList<GerenciaQuery5> resultados = reporte5.saldosGenerales();

        for (int i = 0; i < resultados.size(); i++) {
            System.out.println(resultados.get(i).getNo_cuenta()+ " " + resultados.get(i).getNombre()+ resultados.get(i).getSaldo());
            dataset.setValue(resultados.get(i).getSaldo(), resultados.get(i).getNombre(), String.valueOf(resultados.get(i).getNo_cuenta()));
        }

        return dataset;
    }

